#include <stdio.h>

int	ft_str_is_uppercase(char *str)
{
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			str++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}

int	main(void)
{
	int maj;
	char str[] = "123";

	maj = ft_str_is_uppercase(&str[0]);
	printf("%d\n", maj);
}
